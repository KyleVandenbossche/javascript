/* Ternary Operator 

A ternary operator is implemented for a very basic decision making

*/ 

const age = 33;
const ticket = age < 13 ? "Child" : "Adult";

/* If age is less than 13, ticket is "Child"
Otherwise, ticket is adult


If statement below does the same exact thing

*/

let ticket;
if(age < 13){
    ticket = "child";

} else {
    ticket = "adult";
}


